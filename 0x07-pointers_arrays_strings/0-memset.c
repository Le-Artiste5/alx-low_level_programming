#include "main.h"
/**
 *_memset- fill block of memory with a value
 *@s: starting address of memory to be filled
 *@b: Desired value
 *@n: number of bytes to be changed
 *
 *Return: Change array with new value for n bytes
 */
char *_memset(char *s, char b, unsigned int)
{
int i = 0;
for (; n > 0; i++)
{
s[i] = b;
n--;
}
return (s);
}
